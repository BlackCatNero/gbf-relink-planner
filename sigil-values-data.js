/** %는 소수 첫째 자리까지(.0은 정수로), 그 외(공격력·HP·스턴치 등)는 정수로 표기 */
function _svRound(v, unit) {
  if (unit === "%") {
    const r = Math.round(v * 10) / 10;
    return Number.isInteger(r) ? String(r) : r.toFixed(1);
  }
  return String(Math.round(v));
}

/** 합산 레벨 level에서의 수치를 계산해 반환(숫자). 레벨 무관/flat는 pts 없이 처리. */
function sigilValueAt(entry, level) {
  const pts = entry.pts;
  const L = Number(level) || 0;
  if (L <= 0 || !pts || !pts.length) return null;
  const maxLv = pts[pts.length - 1][0];
  const maxVal = pts[pts.length - 1][1];
  if (L >= maxLv) {
    return entry.cap15 ? maxVal : maxVal * (L / maxLv);
  }
  let prevL = 0, prevV = 0;
  for (const [pl, pv] of pts) {
    if (L <= pl) return prevV + (pv - prevV) * (L - prevL) / (pl - prevL);
    prevL = pl; prevV = pv;
  }
  return maxVal;
}

/**
 * 스킬 리스트에 표시할 FextraLife 수치 문구.
 * @param {string} name  SKILL_CATALOG의 한국어 스킬명
 * @param {number} level 합산 레벨
 * @returns {string} 예 "크리티컬 확률 +38%" · 붙어 있는 게 없거나 매칭 표가 없으면 ""
 */
function sigilValueText(name, level) {
  const entry = SIGIL_VALUES[name];
  if (!entry) return "";
  if ((Number(level) || 0) <= 0) return "";
  if (entry.t) return entry.t;                                  // 레벨 무관 고정 문구
  if (entry.flat != null) {                                     // 레벨 무관 고정 수치
    return `${entry.label} ${entry.sign}${_svRound(entry.flat, entry.unit)}${entry.unit}`;
  }
  const v = sigilValueAt(entry, level);
  if (v == null) return "";
  return `${entry.label} ${entry.sign}${_svRound(v, entry.unit)}${entry.unit}`;
}
